<h4>Upgrading</h4>
Using an older version?. To upgrade simply download the newest version and overwrite all Cjax files,  in most cases that should be enough to upgrade to a newer version.<br />
To be sure, see release notes or <a target='_blank' href='http://cjax.svn.sourceforge.net/viewvc/cjax/CHANGELOG.txt?revision=1&view=markup'>changeLog</a> to see if there are any changes that would affect a previous release.
